package thisEx02;

public class IntExample {

	public static void main(String[] args) {
		int var1 = 10;
		int var2 = 012;
		int var3 = 0xA;
		//long var4 = 2_147_483_648l;

		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.printf("%n%d%n", var1);
		System.out.printf("%o%n", var2);
		System.out.printf("%x%n", var3);

	}
}
